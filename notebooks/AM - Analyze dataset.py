# A script to analyze downloaded bird sound files 
# The program checks the metadata itself and returns
# 1) Number of files for a given bird,
# 2) Minimum, maximum, and average length of a file,
# 3) Number of sounds with more than 1 tag,
# 4) Number of sounds with specified quality (e.g. none - 460, A- 102 recordings, B - 230 recordigns, ...).

# Next, it analyze the recordings and:
# 1) Calculate the signal to noise ratio -> return min, max and average values,
# 2) Shows 3 "A" quality bird-songs spectograms, melgrams and sound waves,
# 3) Shows 3 random bird-songs spectograms, melgrams and sound waves,
# ...

